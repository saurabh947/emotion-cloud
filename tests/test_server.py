"""
Integration tests for api/server.py.

The gRPC server and TorchServe are tested in isolation:
- gRPC stubs are generated from emotion.proto by conftest.py.
- TorchServe HTTP calls are intercepted via a patched httpx.AsyncClient.
- No real gRPC socket is opened; we call the servicer methods directly.
"""
from __future__ import annotations

import json
from unittest.mock import AsyncMock, MagicMock, patch

import httpx
import pytest

import emotion_pb2  # generated by conftest.py
from api.server import EmotionServicer
from api.schemas import InferenceResponse
from config.settings import Settings


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _settings(**overrides) -> Settings:
    base = dict(
        grpc_port=50051,
        torchserve_host="localhost",
        torchserve_inference_port=8080,
        model_name="emotion-detector",
    )
    base.update(overrides)
    return Settings(**base)


def _ts_response(**kwargs) -> httpx.Response:
    """Build a fake TorchServe HTTP 200 response."""
    defaults = dict(
        session_id="s1",
        dominant_emotion="happy",
        confidence_scores={"happy": 0.9},
        stress=0.1,
        engagement=0.8,
        arousal=0.5,
        overall_confidence=0.9,
        embedding=[0.0] * 512,
        timestamp_ms=1000,
        buffering=False,
        error="",
    )
    defaults.update(kwargs)
    return httpx.Response(200, json=defaults)


def _make_request(**kwargs) -> emotion_pb2.EmotionRequest:
    defaults = dict(
        session_id="s1",
        video_frame=b"\x00" * (4 * 4 * 3),
        frame_width=4,
        frame_height=4,
        timestamp_ms=1000,
    )
    defaults.update(kwargs)
    return emotion_pb2.EmotionRequest(**defaults)


async def _iter(*items):
    for item in items:
        yield item


# ---------------------------------------------------------------------------
# Tests: StreamEmotion happy path
# ---------------------------------------------------------------------------

class TestStreamEmotionHappyPath:
    async def test_single_frame_returns_result(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = _ts_response()
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        assert len(responses) == 1
        assert responses[0].dominant_emotion == "happy"
        assert responses[0].session_id == "s1"
        assert responses[0].buffering is False
        assert responses[0].error == ""

    async def test_embedding_propagated(self):
        settings = _settings()
        servicer = EmotionServicer(settings)
        embedding = [float(i) for i in range(512)]

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = _ts_response(embedding=embedding)
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        assert list(responses[0].embedding) == pytest.approx(embedding)

    async def test_multiple_frames(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = _ts_response()
            context = MagicMock()

            requests = _iter(
                _make_request(timestamp_ms=1000),
                _make_request(timestamp_ms=2000),
                _make_request(timestamp_ms=3000),
            )
            responses = []
            async for resp in servicer.StreamEmotion(requests, context):
                responses.append(resp)

        assert len(responses) == 3
        assert mock_post.call_count == 3


