class TestHealthCheck:
    async def test_healthy_when_torchserve_returns_200(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "get", new_callable=AsyncMock) as mock_get:
            mock_get.return_value = httpx.Response(200, json={"status": "Healthy"})
            resp = await servicer.HealthCheck(emotion_pb2.HealthRequest(), MagicMock())

        assert resp.healthy is True
        assert resp.model_status == "ready"

    async def test_unhealthy_when_torchserve_returns_503(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "get", new_callable=AsyncMock) as mock_get:
            mock_get.return_value = httpx.Response(503)
            resp = await servicer.HealthCheck(emotion_pb2.HealthRequest(), MagicMock())

        assert resp.healthy is False
        assert resp.model_status == "unavailable"

    async def test_unhealthy_when_torchserve_unreachable(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "get", new_callable=AsyncMock) as mock_get:
            mock_get.side_effect = httpx.ConnectError("refused")
            resp = await servicer.HealthCheck(emotion_pb2.HealthRequest(), MagicMock())

        assert resp.healthy is False
        assert resp.model_status == "unreachable"
