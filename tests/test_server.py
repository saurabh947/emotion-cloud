class TestStreamEmotionErrors:
    async def test_torchserve_connect_error_yields_error_response(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.side_effect = httpx.ConnectError("refused")
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        assert len(responses) == 1
        assert "unavailable" in responses[0].error.lower()

    async def test_torchserve_http_500_yields_error_response(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = httpx.Response(500, text="internal error")
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        assert "500" in responses[0].error

    async def test_handler_error_field_propagated(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = _ts_response(error="model exploded", dominant_emotion="")
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        assert responses[0].error == "model exploded"

    async def test_session_cleanup_called_on_stream_close(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.return_value = _ts_response()
            context = MagicMock()

            async for _ in servicer.StreamEmotion(_iter(_make_request()), context):
                pass

        # cleanup is the second post call (first is inference)
        assert mock_post.call_count == 2
        cleanup_call = mock_post.call_args_list[1]
        body = cleanup_call.kwargs.get("json") or cleanup_call.args[1] if len(cleanup_call.args) > 1 else cleanup_call.kwargs["json"]
        assert body["action"] == "cleanup"
        assert body["session_id"] == "s1"

    async def test_session_cleanup_called_even_on_error(self):
        settings = _settings()
        servicer = EmotionServicer(settings)

        with patch.object(servicer._http, "post", new_callable=AsyncMock) as mock_post:
            mock_post.side_effect = httpx.ConnectError("refused")
            context = MagicMock()

            responses = []
            async for resp in servicer.StreamEmotion(_iter(_make_request()), context):
                responses.append(resp)

        # First call raised ConnectError (yielded error response),
        # second call is cleanup (also will raise, but non-fatal).
        assert mock_post.call_count >= 2


